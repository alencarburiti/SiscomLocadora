    public void getHistoricoLocacao(String nome_cliente, String nome_objeto) {
        pool = new Pool();
        locacaoDAO = new LocacaoDAO(pool);
        itens = null;
        itens = locacaoDAO.getObjetoHistoricoLocacaoes(nome_cliente, nome_objeto);
        mostrar_locacoes(itens);
    }      

    public void mostrar_locacoes(List<ItemLocacao> itemLocacoes) {

        DefaultTableModel tableModel = (DefaultTableModel) jtbl_historico_locacao.getModel();
        tableModel.setNumRows(0);
        if (itemLocacoes.size() == 0) {
            JOptionPane.showMessageDialog(null, "Histórico não encontrado");
            jtf_nome_cliente.requestFocus();
        } else {
            
            for (int i = 0; i < itemLocacoes.size(); i++) {
                
                SimpleDateFormat df_data_hora_locada = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss");                    
                SimpleDateFormat in = new SimpleDateFormat("yyyy-MM-dd");
                SimpleDateFormat out = new SimpleDateFormat("dd/MM/yyyy");
                String data_devolucao = "";
                try {
                    data_devolucao = out.format(in.parse(itemLocacoes.get(i).getData_devolucao().toString()));
                } catch (Exception e) {
                    
                }
                
                Moeda moeda = new Moeda();
                
                DefaultTableModel row = (DefaultTableModel) jtbl_historico_locacao.getModel();
                
                row.addRow(new Object[]{itemLocacoes.get(i).getCopia().getObjeto().getCodigo_objeto(),
                    itemLocacoes.get(i).getLocacao().getDependente().getNome_dependente(),
                    itemLocacoes.get(i).getCopia().getObjeto().getTitulo(),
                    df_data_hora_locada.format(itemLocacoes.get(i).getData_locacao()),
                    data_devolucao,
                    itemLocacoes.get(i).getCopia().getDiaria().getPromocaoLocacao().getDescricao(),
                    itemLocacoes.get(i).getLocacao().getUsuario().getNome_usuario()});
                
            }            
        }
    }

