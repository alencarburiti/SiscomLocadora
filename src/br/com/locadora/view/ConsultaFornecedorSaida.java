    public Fornecedor tbFornecedorLinhaSelecionada(JTable tb) {
        Fornecedor fornecedor = null;

        if (tb.getSelectedRow() != -1) {
            fornecedor = fornecedores.get(tb.getSelectedRow());
        }
        return fornecedor;
    }
    private InterfacePool pool;

    public void getFornecedor() {
        pool = new Pool();
        FornecedorDAO fornecedorDAO = new FornecedorDAO(pool);
        fornecedores = fornecedorDAO.getFornecedorNomeFantasia("%" + jtf_consulta.getText().trim() + "%");
        mostrarFornecedor(fornecedores);
    }

    public void mostrarFornecedor(List<Fornecedor> fornecedores) {
        ((DefaultTableModel) jtbl_fornecedor.getModel()).setRowCount(0);
        jtbl_fornecedor.updateUI();

        if (fornecedores.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nenhum Fornecedor encontrado");
            jtf_consulta.requestFocus();

        } else {
            for (int i = 0; i < fornecedores.size(); i++) {

                DefaultTableModel row = (DefaultTableModel) jtbl_fornecedor.getModel();

                row.addRow(new Object[]{fornecedores.get(i).getCodigo_fornecedor(),
                    fornecedores.get(i).getNome_fantasia(),
                    fornecedores.get(i).getCnpj(),
                    fornecedores.get(i).getNome_vendedor()});
            }
            jtbl_fornecedor.requestFocus();
            jtbl_fornecedor.setSelectionMode(0);
        }
    }

