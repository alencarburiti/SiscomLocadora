    public void botaoOK(JTable tb) {
        if (tb != null) {
            Fornecedor fornecedor = tbFornecedorLinhaSelecionada(jtbl_fornecedor);
            if ((janelapai != null) && (fornecedor != null)) {
                janelapai.carregarFornecedor(fornecedor);
                retornaJanelaPai();

                setVisible(false);

            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um Fornecedor");
        }
    }


