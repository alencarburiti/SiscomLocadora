    public void mostrarCategorias() {
        DefaultTableModel tableModel = (DefaultTableModel) jtbl_categoria.getModel();
        tableModel.setNumRows(0);

        categorias = new ArrayList<>();
        pool = new Pool();
        categoriaDAO = new CategoriaDAO(pool);
        categorias = categoriaDAO.getCategoria();

        if (categorias.size() > 0) {

            for (int i = 0; i < categorias.size(); i++) {

                DefaultTableModel row = (DefaultTableModel) jtbl_categoria.getModel();
                row.addRow(new Object[]{categorias.get(i).getDescricao()});
            }
        } else {
            JOptionPane.showMessageDialog(null, "Nenhuma Categoria encontrada.");
        }

    }

