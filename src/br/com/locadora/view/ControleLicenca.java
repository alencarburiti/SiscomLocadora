    public void registrarChave(String key_licenca) {
        try {
            EncriptaDecriptaAES enc = new EncriptaDecriptaAES();
            String key = "0123456789abcdef";

            byte[] textoencriptado = enc.toHex(key_licenca);
//            byte[] textoencriptado = key_licenca.getBytes();

            String textodecriptado = enc.decrypt(textoencriptado, key);
            System.out.println("Texto Decriptado: " + textodecriptado);

            String dados[] = textodecriptado.split("\\|");

            InfoSystem infoSystem = new InfoSystem();
            infoSystem.setData_aquisicao(new SimpleDateFormat("dd/MM/yyyy").parse((String) dados[0].trim()));
            infoSystem.setQtd_licenca(Integer.parseInt(dados[1].trim()));
            infoSystem.setCnpj(jtf_cnpj.getText());
            infoSystem.setNome_empresa(jtf_nome_empresa.getText());
            infoSystem.setData_inicio_licenca(new SimpleDateFormat("dd/MM/yyyy").parse((String) dados[2].trim()));
            infoSystem.setData_final_licenca(new SimpleDateFormat("dd/MM/yyyy").parse((String) dados[3].trim()));
            infoSystem.setChave(jtf_chave_licenca.getText().trim());

            pool = new Pool();
            InfoSystemDAO infoDAO = new InfoSystemDAO(pool);
            infoDAO.atualizar(infoSystem);

            mostrarInformacoes();
            JOptionPane.showMessageDialog(null, "Registrado com sucesso!");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

