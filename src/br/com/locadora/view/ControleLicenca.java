    public void mostrarInformacoes() {
        try {
            
        pool = new Pool();
        InfoSystemDAO infoDAO = new InfoSystemDAO(pool);

        List<InfoSystem> infos = infoDAO.getSystemInfo();

        if (infos.size() > 0) {
            jtf_nome_empresa.setText(infos.get(0).getNome_empresa());
            jtf_cnpj.setText(infos.get(0).getCnpj());
            jtf_quantidade_licenca.setText(infos.get(0).getQtd_licenca().toString());
            jtf_quantidade_licenca_ativa.setText(getStationOpen().toString());
            jtf_chave_licenca.setText(infos.get(0).getChave());
            
            SimpleDateFormat in = new SimpleDateFormat("yyyy-MM-dd");
            SimpleDateFormat out = new SimpleDateFormat("dd/MM/yyyy");
            SimpleDateFormat df_data_hora_locada = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss");
            
            String data_aquisicao = null;
            String data_inicio_licenca = null;
            String data_final_licenca = null;
            try {
                data_aquisicao = out.format(in.parse(infos.get(0).getData_aquisicao().toString()));
                data_inicio_licenca = out.format(in.parse(infos.get(0).getData_inicio_licenca().toString()));
                data_final_licenca = out.format(in.parse(infos.get(0).getData_final_licenca().toString()));                
            } catch (ParseException ex) {
                //Logger.getLogger(CadastraAlteraCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
            jtf_data_aquisicao.setText(data_aquisicao);
            jtf_data_inicio_licenca.setText(data_inicio_licenca);
            jtf_data_final_licenca.setText(data_final_licenca);
            jtf_data_ultimo_acesso.setText(df_data_hora_locada.format(infos.get(0).getData_ultimo_acesso()));            
        }
        } catch (Exception e) {
        }
        
    }

